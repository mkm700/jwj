package misc;

public class Mod {

	public static void main(String[] args) {
		for (int i = -2; i < 5; i++) {
			System.out.println(i + " % 2 = " + i % 2);
			System.out.println("2 % " + i + " = " + 2 % i);
			System.out.println(i + " % 3 = " + i % 3);
			System.out.println(i + " % 4 = " + i % 4);
			System.out.println(i + " % 5 = " + i % 5);
		}
		System.out.println();
		

	}

}
